#include<stdio.h>
int main()
{
    int a; // a full number with no floating point
    a = -10;
    printf("%d",a);
    float b;
    b = 53.5; // a fractional number
    printf("\n%.2f", b);
    double c; // a larger fractional number
    c = 15.5;
    printf("\n%lf", c);
    unsigned int x;  // int with value > 0
    x = 10;
    printf("\n%u", x);
    char d; // a single letter
    d='A';
    printf("\n%c",d);
//department of CSE
}
